// TEMPORARY local-testing helper (issue #222). See ../MIGRATION_TEMP.md §3.
//
// Drops a throwaway `.ts` file into the `src/` of chosen packages so you have a
// real change to exercise `yarn changeset` / `yarn changeset version` locally and
// inspect the resulting version bumps + CHANGELOG updates.
//
// ⚠️⚠️  THE FILES IT CREATES MUST NEVER BE COMMITTED OR PUSHED. Local testing only.
//        Clean up with `--clean`, then `git reset --hard`. See the docs.
//
// Usage:
//   node migration-temp/add-test-change.mjs                 # interactive: pick packages
//   node migration-temp/add-test-change.mjs --all           # every package
//   node migration-temp/add-test-change.mjs --packages platform-js,compact-js
//   node migration-temp/add-test-change.mjs --clean         # remove the test files again

import { execFileSync } from 'node:child_process';
import { existsSync, rmSync, writeFileSync } from 'node:fs';
import { basename, join } from 'node:path';
import { createInterface } from 'node:readline';
import { parseArgs } from 'node:util';

const TEST_FILE = '__changeset_test__.ts';

const CONTENT =
  [
    '// ⚠️  LOCAL CHANGESET TEST FILE — DO NOT COMMIT. ⚠️',
    '// Generated by migration-temp/add-test-change.mjs to create a throwaway change',
    '// for exercising `yarn changeset version` locally. Remove it with:',
    '//   node migration-temp/add-test-change.mjs --clean',
    '// then run `git reset --hard` to undo any version / CHANGELOG edits.',
    '// See MIGRATION_TEMP.md §3.',
    '',
    "export const __changesetTest = 'local-testing-only';",
  ].join('\n') + '\n';

const { values } = parseArgs({
  options: {
    packages: { type: 'string' },
    all: { type: 'boolean' },
    clean: { type: 'boolean' },
  },
  strict: true,
});

const workspaces = execFileSync('yarn', ['workspaces', 'list', '--json'], { encoding: 'utf8' })
  .trim()
  .split('\n')
  .map((line) => JSON.parse(line))
  .filter((ws) => ws.location !== '.');

const targetFile = (ws) => join(ws.location, 'src', TEST_FILE);

const matches = (ws, token) =>
  ws.name === token || basename(ws.location) === token || ws.name.endsWith(`/${token}`);

const prompt = async (question) => {
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  try {
    return (await new Promise((resolve) => rl.question(question, resolve))).trim();
  } finally {
    rl.close();
  }
};

const resolveToken = (token) => {
  const byIndex = /^\d+$/.test(token) ? workspaces[Number(token) - 1] : undefined;
  const ws = byIndex ?? workspaces.find((w) => matches(w, token));
  if (!ws) throw new Error(`Unknown package: ${token}`);
  return ws;
};

const main = async () => {
  // --- clean mode: remove every test file we may have created --------------
  if (values.clean) {
    const removed = workspaces
      .map(targetFile)
      .filter((f) => existsSync(f))
      .map((f) => {
        rmSync(f);
        return f;
      });
    console.log(removed.length ? `Removed:\n  ${removed.join('\n  ')}` : 'No test files found.');
    console.log('\nReminder: run `git reset --hard` to undo any changeset version / CHANGELOG edits.');
    return;
  }

  // --- choose which packages get the test file -----------------------------
  const chosen = values.all
    ? workspaces
    : values.packages
      ? values.packages
          .split(',')
          .map((s) => s.trim())
          .filter(Boolean)
          .map(resolveToken)
      : await (async () => {
          if (!process.stdin.isTTY) throw new Error('No TTY: pass --packages <list> or --all.');
          console.log('Packages:');
          workspaces.forEach((ws, i) => console.log(`  [${i + 1}] ${ws.name}  (${ws.location})`));
          const ans = await prompt('\nAdd test file to which? (comma numbers/names, or "all"): ');
          if (ans.toLowerCase() === 'all') return workspaces;
          return ans
            .split(',')
            .map((s) => s.trim())
            .filter(Boolean)
            .map(resolveToken);
        })();

  if (!chosen.length) {
    console.log('Nothing selected.');
    return;
  }

  const written = chosen.map((ws) => {
    const dir = join(ws.location, 'src');
    if (!existsSync(dir)) throw new Error(`No src/ dir for ${ws.name} (${dir})`);
    const file = targetFile(ws);
    writeFileSync(file, CONTENT);
    return file;
  });

  console.log(`\n✅ Wrote test file to ${written.length} package(s):`);
  written.forEach((f) => console.log(`  ${f}`));
  console.log('\n⚠️  DO NOT COMMIT / PUSH these files — local testing only.');
  console.log('\nNext (local changeset test):');
  console.log('  1. yarn dist               # build everything first (from repo root)');
  console.log('  2. yarn changeset          # pick the same package(s) + bump + summary');
  console.log('  3. yarn changeset version  # apply bumps -> inspect package.json + CHANGELOG');
  console.log('  4. git diff                # review what a real release would change');
  console.log('\nCleanup (NOTHING here should be committed):');
  console.log('  node migration-temp/add-test-change.mjs --clean   # delete the test files');
  console.log('  rm .changeset/*.md  (except config.json/README.md) # if a test changeset remains');
  console.log('  git reset --hard                                  # undo version/CHANGELOG/lockfile edits');
};

main().catch((err) => {
  console.error(`\n✗ ${err.message}`);
  process.exit(1);
});
